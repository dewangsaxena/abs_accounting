        $quantity = $inventory[$item_id];
        $is_successful = $statement -> execute([':item_id' => $item_id, ':quantity' => $quantity, ':store_id' => StoreDetails::SLAVE_LAKE]);
        if($is_successful !== true && $statement -> rowCount() < 1) throw new Exception("Cannot update for: $item_id with $quantity<br>");
    }
    $db -> commit();
    echo 'All Adjusted';
}
catch(Exception $e) {
    if($db -> inTransaction()) $db -> rollBack();
    echo $e -> getMessage();
}